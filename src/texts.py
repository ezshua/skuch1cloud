def get_welcome_message(user_dir_name: str, total_size_str: str, files_count: int) -> str:
    """
    Генерирует приветственное сообщение с описанием возможностей бота.

    Args:
        user_dir_name (str): Имя папки пользователя.
        total_size_str (str): Отформатированный общий размер файлов пользователя.
        files_count (int): Количество сохраненных файлов пользователя.

    Returns:
        str: Отформатированный текст приветственного сообщения с поддержкой HTML.
    """
    return (
        f"👋 <b>Привет! Я бот для удобного сохранения твоих файлов.</b>\n\n"
        f"📁 Твоя личная папка: <b>{user_dir_name}</b> ({total_size_str} в {files_count} файлах)\n\n"
        f"<b>🚀 Мои возможности:</b>\n"
        f"• Сохраняю любые документы, фото, видео и аудио (размером до 20 МБ).\n"
        f"• Автоматически привожу имена файлов в порядок для безопасного хранения.\n"
        f"• Отслеживаю дубликаты и общий лимит дискового пространства.\n\n"
        f"<b>🛠 Доступные команды:</b>\n"
        f"• /start — перезапустить бота и показать это сообщение.\n"
        f"• /status — посмотреть список загруженных файлов и свободное место.\n"
        f"• /delete <code>имя_файла</code> — удалить указанный файл из хранилища.\n\n"
        f"<b>💡 Реакции на действия:</b>\n"
        f"1. <b>Отправь файл или медиа</b> — я скачаю его и сохраню в твою папку.\n"
        f"2. <b>Отправь точное имя сохраненного файла</b> (из списка /status) — я пришлю этот файл тебе обратно.\n"
        f"3. <b>Отправь любой другой текст</b> — я отвечу режимом \"эхо\".\n"
        f"4. <b>При удалении</b> — я попрошу подтверждение (напиши \"да\" или \"yes\")."
    )